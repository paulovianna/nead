<h1 class="titulo">
	<span class="glyphicon glyphicon-arrow-right"></span>
	Técnico em Informática para Internet
</h1>
<div class="row texto fundo">
	<div class="col-sm-12 col-md-12">
		<p>O Curso Técnico em Informática para Internet visa formar profissionais de nível técnico com perfil dinâmico, inovador e ético, capazes de formular soluções para sistemas de informação computacional, estando aptos a avaliar, diagnosticar, projetar, implementar e manter sistemas de comunicação no escopo da Internet e redes de computadores.</p>
	</div>
</div>
<div class="row texto fundo">
	<div class="col-sm-6 col-md-6">
		<h3>Objetivos Específicos</h3>
		<p>Instrumentalizar alunos na área de informática, propiciando condições de inserção no mundo do trabalho em áreas de atuação tanto específicas de informática como outras áreas que demandem conhecimentos de informática;</p>
		<p>Formar profissionais de nível técnico em Informática, para atuar em empresas de pequeno, médio e grande porte, ou como profissionais autônomos, com ética e dinamismo;</p>
		<p>Atender demandas específicas dos municípios cadastrados no programa e-Tec Brasil, qualificando e habilitando trabalhadores para atuarem no mercado de trabalho em suas regiões;</p>
		<p>Capacitar o aluno para atuação na área de produção de soluções de software para a Internet, executando atividades de projeto, criação e manutenção de páginas de informações;</p>
		<p>Criar tecnologia educacional na formação e capacitação de profissionais para atuar na modalidade de educação à distância;</p>
		<p>Desenvolver metodologias pedagógicas e material didático direcionados à modalidade de educação à distância na área de informática.</p>
	</div>
	<div class="col-sm-6 col-md-6">
		<h3>Áreas de Atuação</h3>
		<p>De acordo com o perfil profissional definido, egresso do Curso Técnico em Informática para Internet subsequente ao Ensino Médio do Colégio Agrícola de Frederico Westphalen, está capacitado a atuar nas seguintes áreas:</p>
		<p>Desenvolvimento de programas de computador para Internet;</p>
		<p>Utilização de ferramentas de desenvolvimento e linguagens de programação para web;</p>
		<p>Desenvolvimento de páginas web;</p>
		<p>Construção interfaces e aplicativos empregados no comércio e marketing eletrônicos;</p>
		<p>Manutenção de sítios e portais na Internet e na Intranet;</p>
		<p>Orientação de os usuários para correta utilização de softwares;</p>
		<p>Configuração e gerenciamento de acessos de usuários em redes de computadores;</p>
		<p>Configuração de serviços de redes de computadores.</p>
