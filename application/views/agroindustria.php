<h1 class="titulo">
	<span class="glyphicon glyphicon-arrow-right"></span>
	Técnico em Agroindústria
</h1>
<div class="row texto fundo">
	<div class="col-sm-12 col-md-12">
		<p>O Curso Técnico em Agroindústria na modalidade a distância tem por objetivo formar profissionais com o propósito principal de atender à crescente demanda das diferentes regiões brasileiras na conservação e na industrialização de produtos de origem vegetal e animal, para indústrias de pequeno, médio e grande porte, bem como capacitar para o uso correto das ferramentas de gestão agroindustrial, respeitando as características regionais.</p>
	</div>
</div>
<div class="row texto fundo">
	<div class="col-sm-6 col-md-6">
		<h3>Objetivos Específicos</h3>
		<p>Oportunizar condições de profissionalização aos alunos que já concluíram o Ensino Médio;</p>
		<p>Oportunizar a requalificação de profissionais, a fim de acompanhar as tendências tecnológicas;</p>
		<p>Maximizar a utilização da infraestrutura institucional, ampliando o número de habilitações ofertadas;</p>
		<p>Proporcionar a habilitação profissional, em curto prazo, observando-se as exigências e expectativas da comunidade regional;</p>
		<p>Atuar como agente de incentivo à instalação e melhoria da qualidade das agroindústrias da região;</p>
		<p>Reduzir as perdas de produtos agrícolas, através da transformação dos mesmos em nível da propriedade rural;</p>
		<p>Oportunizar uma maior oferta de produtos de origem animal e vegetal processados;</p>
		<p>Cumprir a função social da escola, colaborando com a melhoria das condições de vida no campo, com diminuição do êxodo rural;</p>
		<p>Oportunizar uma formação profissional, que permita uma alternativa de empreendedorismo e trabalho aos alunos;</p>
		<p>Oportunizar a educação permanente e a requalificação, atendendo aos novos paradigmas que estabelecem a necessidade e a capacidade de mobilidade no mundo do trabalho;</p>
		<p>Colocar à disposição da sociedade um profissional apto ao exercício de suas funções e consciente de suas responsabilidades;</p>
		<p>Integrar o ensino ao trabalho oportunizando o desenvolvimento das condições para a vida produtiva moderna;</p>
		<p>Oportunizar uma profissionalização rápida, para atividades específicas e delimitadas do mundo do trabalho, através de um currículo disciplinar específico.</p>
	</div>
	<div class="col-sm-6 col-md-6">
		<h3>Áreas de Atuação</h3>
		<p>O Técnico em Agroindústria poderá atuar de forma autônoma ou em agroindústrias, como responsável técnico em linhas de produção nas áreas de processamento de produtos de origem animal e vegetal; em laboratórios de controle de qualidade de matérias-primas, processos e produtos alimentícios; em associações e cooperativas, gerenciando a elaboração, execução e avaliação de projetos agroindustriais; como consultor técnico em atividades agroindustriais; na área comercial, em estabelecimentos de hortifrutigranjeiros; e como gestor de empreendimentos agroindustriais próprios.</p>
